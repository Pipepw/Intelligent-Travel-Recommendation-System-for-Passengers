package com.newweather.intelligenttravel.Gson;

public class LalLocation {
    private String lng;
    private String lat;
    public void setLng(String lng) {
        this.lng = lng;
    }
    public String getLng() {
        return lng;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }
    public String getLat() {
        return lat;
    }
}
